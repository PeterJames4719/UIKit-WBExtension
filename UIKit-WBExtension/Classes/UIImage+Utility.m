+ (UIImage *)ext_constituteImages:(NSArray *)images {
    CGFloat imgHeight = 0.0;
    CGFloat imgWidth = 0.0;
    
    for (UIImage *img in images) {
        CGSize eachSize = img.size;
        if (eachSize.width > imgWidth) {
            imgWidth = eachSize.width;
        }
        imgHeight += eachSize.height;
    }
    
    CGRect rect = CGRectMake(0.0f, 0.0f, imgWidth, imgHeight);
    
    UIGraphicsBeginImageContextWithOptions(rect.size, NO, [UIScreen mainScreen].scale);
    //CGContextRef context = UIGraphicsGetCurrentContext();
    CGFloat offsetY = 0;
    for (UIImage *img in images) {
        CGSize eachSize = img.size;
        imgWidth = eachSize.width;
        [img drawInRect:CGRectMake(0, offsetY, imgWidth, eachSize.height)];
        offsetY += eachSize.height;
    }
    
    UIImage *image = UIGraphicsGetImageFromCurrentImageContext();
    UIGraphicsEndImageContext();
    
    return image;
}

